//! Create and configure AI agent spending accounts on Squads Protocol v4.
//!
//! The core entry point is [`create_governed_spending_account`], which performs
//! four sequential on-chain transactions (TX1–TX4):
//!
//! 1. **TX1** — `multisig_create_v2`: creates the multisig with a temporary
//!    bootstrap `configAuthority` equal to `payer.pubkey()`.
//! 2. **TX2** — `multisig_add_spending_limit`: attaches a per-period lamport cap,
//!    signed by the bootstrap `configAuthority`.
//! 3. **TX3** — `multisig_change_threshold`: sets the governance threshold,
//!    signed by the bootstrap `configAuthority`.
//! 4. **TX4** — `multisig_set_config_authority`: permanently disables the
//!    `configAuthority` by setting it to [`Pubkey::default()`].
//!
//! # Bug fixes preserved from the TypeScript proof-of-concept
//!
//! - **BUG FIX #1**: Treasury is always read from the live on-chain
//!   [`squads_multisig_program::state::ProgramConfig`] account, never assumed
//!   to be the payer's own keypair.
//! - **BUG FIX #2**: [`Period`] uses the Rust enum variant (e.g. `Period::Day`),
//!   never a raw struct literal or integer constant.
//! - **BUG FIX #3**: The `configAuthority` null sentinel is [`Pubkey::default()`]
//!   (the all-zero System Program address), not a language-level `None`/`null`.

